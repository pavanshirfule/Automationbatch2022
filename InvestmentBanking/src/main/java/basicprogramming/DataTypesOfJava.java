package basicprogramming;

public class DataTypesOfJava {
	
	public static void main(String[] args) {
		
		//Premative date type
		byte no;						//use sign + or -		// 1-byte
		short shortinfo;										// 2-byte
		int rollno;												// 4-byte
		long longval;											// 8-byte
		float floatval;											// 4-byte
		double doubleval;										// 8-byte
		char charval;											// 2-byte
		boolean booleanval;										// 1-bit
		
		no=-122;
		shortinfo=45;
		rollno=256;
		longval=749925;
		floatval=12.0265f;
		doubleval=4520.26d;
		charval='A';
		booleanval=true;
		
		System.out.println(no);
		System.out.println(shortinfo);
		System.out.println(rollno);
		System.out.println(longval);
		System.out.println(floatval);
		System.out.println(doubleval);
		System.out.println(charval);
		System.out.println(booleanval);
		
		
		//Non premetive
		String name;
		name="hsgxtu.....shsyg";
		
		System.out.println(name);
	}

}
